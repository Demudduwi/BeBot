	function __construct (&$bot)
	{
		// Initialize the base module
		parent::__construct($bot, get_class($this));

		// Register command
		$this->register_command('all', 'boss', 'GUEST');
		$this -> register_alias("boss", "bosses");		
		$this->register_command('all', 'bossloot', 'GUEST');

		// Add description/help
		$this -> help['description'] = "Search and show boss loots and their details.";
		$this -> help['command']['boss <param(s)>'] = "Search for existing boss to see its loot.";
		$this -> help['command']['bossloot <param(s)>'] = "Search for existing loot to see its boss.";		
	}

	function command_handler($source, $msg, $origin)
	{
		$this->error->reset();
		
		$vars = explode(' ', strtolower($msg));
		$command = $vars[0];

		switch($command)
		{
			case 'boss':
				return $this -> send_boss($source, $origin, "boss", strtolower($msg));
				break;
			case 'bossloot':
				return $this -> send_boss($source, $origin, "bossloot", strtolower($msg));
				break;
			default:				
				$this -> error -> set("Broken plugin, received unhandled command: $command");
				return($this->error->message());
		}
	}
	
