	function __construct (&$bot)
	{
		// Initialize the base module
		parent::__construct($bot, get_class($this));

		// Register command
		$this->register_command('all', 'perks', 'GUEST');
		$this -> register_alias("perks", "perk");

		// Add description/help
		$this -> help['description'] = "Show SL perks and their buffs per level/profession";
		$this -> help['command']['perks <level> <profession>'] = "Display SL perks available at level/profession given";
	}

	function command_handler($source, $msg, $origin)
	{
		$this->error->reset();
		
		$vars = explode(' ', strtolower($msg));
		$command = $vars[0];

		switch($command)
		{
			case 'perks':
				if(isset($vars[1])&&is_numeric($vars[1])&&$vars[1]>0&&isset($vars[2])&&$vars[2]!="") {
					return $this -> send_perks($source, $origin, $vars[1], $vars[2]);
				} else {
					$this->bot->send_help($source);
				}
				break;		
			default:				
				$this -> error -> set("Broken plugin, received unhandled command: $command");
				return($this->error->message());
		}
	}
	
